		function ssSwiper(){
		  if( $(window).outerWidth() >= (768) ) {            
		    // console.log('noswiper');
		    if (ss_Swiper != undefined) {
			    ss_Swiper.destroy(true, true);
			    ss_Swiper = undefined;
		    }
		  } else if( $(window).outerWidth() < (768) ){
		    // console.log('swiper');
		    if (ss_Swiper != undefined) {
		    	// console.log('!=undef');
		    } else {
		    	// console.log('=undef');
			    ss_Swiper = new Swiper('#ss-swiper', {            
			        loop: true,
			        slidesPerView: 'auto',
			        centeredSlides: true,
			    });
		    }
		  }			
		}

		ssSwiper();
	  $(window).on('resize', function(){
			ssSwiper();
	  });

